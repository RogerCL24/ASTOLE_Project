class InputAlert(BaseModel):
    """Alert JSON sent by Engineer 1 to the multi-agent pipeline."""

    alert_id: str = Field(..., description="Unique alert ID (e.g. AST-2026-001)")
    timestamp: str = Field(..., description="ISO 8601 detection timestamp")
    window_id: str = Field(default="", description="60s window ID for correlation")
    gnn_metadata: GNNMetadata
    network_data: NetworkData
    @model_validator(mode="before")
    @classmethod
    def merge_technical_details_into_network_data(cls, data: Any) -> Any:
        """Accept v1.1 payloads where flow metrics are inside `technical_details`.

        Engineer 1 may send:
        - `network_data`: connection identity (IPs, ports, protocol, l7_proto)
        - `technical_details`: flow metrics (duration_ms, bytes, pkts, tcp_flags)

        Internal processing expects these metrics in `network_data`, so we merge
        them before field-level validation.
        """
        if not isinstance(data, dict):
            return data
        network_data = dict(data.get("network_data", {}) or {})
        technical_details = dict(data.get("technical_details", {}) or {})
        if technical_details:
            for key in ("duration_ms", "in_bytes", "out_bytes", "tcp_flags", "in_pkts", "out_pkts"):
                if key in technical_details and key not in network_data:
                    network_data[key] = technical_details[key]
            data["network_data"] = network_data
        return data

    top_features: TopFeatures = Field(default_factory=TopFeatures)
    window_stats: WindowStats = Field(default_factory=WindowStats)

    @field_validator("timestamp")
    @classmethod
    def validate_timestamp(cls, v: str) -> str:
        try:
            datetime.fromisoformat(v.replace("Z", "+00:00"))
        except ValueError as e:
            raise ValueError(f"timestamp must be valid ISO 8601: {e}")
        return v
